
public class vendas {

	private int pedido;
	private int codcli;
	private int codven;
	private float vtotal;
	public int getPedido() {
		return pedido;
	}
	public void setPedido(int pedido) {
		this.pedido = pedido;
	}
	public int getCodcli() {
		return codcli;
	}
	public void setCodcli(int codcli) {
		this.codcli = codcli;
	}
	public int getCodven() {
		return codven;
	}
	public void setCodven(int codven) {
		this.codven = codven;
	}
	public float getVtotal() {
		return vtotal;
	}
	public void setVtotal(float vtotal) {
		this.vtotal = vtotal;
	}
	
	
}
